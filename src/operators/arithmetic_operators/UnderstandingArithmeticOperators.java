package operators.arithmetic_operators;

public class UnderstandingArithmeticOperators {
    public static void main(String[] args) {

        System.out.println(3 + 5);
        System.out.println(5 - 2);
        System.out.println(10 / 2);
        System.out.println(2 * 4);
        System.out.println(10 % 3);

        System.out.println("\n -------- \n");

        int num1 = 6, num2 = 4;
        System.out.println("The sum of numbers is = " + (num1 + num2));
        System.out.println("The subtraction of number is = " + (num1 - num2));
        System.out.println("The product of number is = " + num1 * num2);
        System.out.println("The division of number is = " + num1 / num2);
        System.out.println(num1 % num2);
    }
}
