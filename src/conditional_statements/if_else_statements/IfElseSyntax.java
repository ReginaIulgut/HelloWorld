package conditional_statements.if_else_statements;

public class IfElseSyntax {
    public static void main(String[] args) {
        /*
        - if or else are used to control the flow of the program based on condition
        - conditions are always boolean
        - if block can be used without else
        - else block never can be used without if
         */
        boolean condition = true;

        if(condition){
            // If block and it executes if the condition is true

        }
        else{
            // Else block and it executes if the condition is false

        }
        System.out.println();


    }
}
