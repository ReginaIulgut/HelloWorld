package primitives;

public class ObjectTypes {
    public static void main(String[] args) {
        /*
        any data that cannot be stored with primitives should be handled using objects
        objects = reference types = non-primitives
         */

        String name = "Regina";
        String lName = "Iulgutlina";
        String DOB = "Dec 15,1995";
        String SSN = "0000-000-000";
        String phoneNumber = "(850) 586-8802";

        System.out.println("My number is " + phoneNumber); // My number is (850) 586-8802
    }
}
